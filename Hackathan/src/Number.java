
public class Number
{
	public static void main(String[] args) 
	{
		int n =50;
		print(n);
	}
	private static void print(int n) 
	{
		
		for(int i=1;i<=n;i++)
		{
			if(((i%3)==0) && ((i%5)==0))
			{
				System.out.println("whazaa");
			}
			else if((i%3)==0)
			{
				System.out.println("hip");
			}	
			else if((i%5)==0)
			{
				System.out.println("hop");
			}
			else
			{
				System.out.println(i);
			}
		}
	}
}
